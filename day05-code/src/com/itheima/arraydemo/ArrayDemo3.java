package com.itheima.arraydemo;

public class ArrayDemo3 {
    public static void main(String[] args) {
        // 1. 定义数组
        int [] arr = {1,2,3,4,5};
        // 2. 利用循环获取数组中的元素
        for(int i = 0; i<5; i++){
            System.out.println(arr[i]);
        }

        // 3. arr.length
        for(int i =0; i< arr.length; i++){
            System.out.println(arr[i]);
        }
        // 4. 自动快速生成数组的遍历方式 数组名.fori



    }
}
